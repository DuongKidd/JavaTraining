package com.duong.training.controller;

import com.duong.training.dto.RoleDTO;
import com.duong.training.service.RoleService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import java.util.List;

@RestController
@RequestMapping("/")
public class RoleController {

    @Autowired
    private RoleService roleService;

    @GetMapping("role/username/{username}")
    public ResponseEntity<List<RoleDTO>> findRoleByUsername(@PathVariable("username") String username) {
        List<RoleDTO> roleDTOs = roleService.getAllRoleByUserName(username);
        return ResponseEntity.ok(roleDTOs);
    }

}
